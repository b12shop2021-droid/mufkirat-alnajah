/* اختيار اقتباس اليوم بناءً على يوم السنة + المزاج الأخير */
export function getDailyQuote(lastMoodIdx?: number): Quote {
  const dayOfYear = Math.floor(
    (Date.now() - new Date(new Date().getFullYear(), 0, 0).getTime()) / 86_400_000,
  );

  /* فلتر حسب المزاج إذا متاح */
  const pool =
    lastMoodIdx !== undefined
      ? QUOTES.filter((q) => !q.mood || q.mood.includes(lastMoodIdx))
      : QUOTES;

  return pool[dayOfYear % pool.length];
}
